#!/usr/bin/env node

/**
 * Generate static Markdown API documentation from docs/api/openapi.json.
 *
 * Pipeline:
 *   docs/api/openapi.json  →  widdershins  →  docs/api/README.md
 *
 * Run this file directly:
 *   node scripts/generate-api-markdown.js
 *
 * Or via the npm/pnpm scripts defined in the root package.json:
 *   pnpm docs:markdown    – runs only this step
 *   pnpm docs             – full pipeline: OpenAPI JSON → Markdown
 *
 * ─── What widdershins does ───────────────────────────────────────────────────
 * widdershins is an open-source tool that reads an OpenAPI 3.x (or Swagger 2.x)
 * file and renders it to Markdown using a set of Mustache/Nunjucks templates.
 * The output is a single Markdown document with:
 *   - A Table of Contents automatically built from tag and path groupings
 *   - Per-endpoint sections with method, path, description, and status codes
 *   - Request-body and response schemas pulled from `components/schemas`
 *   - Code-sample blocks (curl, JavaScript, Python, …) generated per endpoint
 *   - Authentication / security scheme documentation
 *
 * All options below are passed to the `widdershins.convert()` API directly.
 * See: https://github.com/Mermade/widdershins#options
 * ─────────────────────────────────────────────────────────────────────────────
 */

'use strict';

const fs   = require('fs');
const path = require('path');
const widdershins = require('widdershins');

// ── Paths ──────────────────────────────────────────────────────────────────

const ROOT_DIR    = path.resolve(__dirname, '..');
const INPUT_PATH  = path.join(ROOT_DIR, 'docs', 'api', 'openapi.json');
const OUTPUT_DIR  = path.join(ROOT_DIR, 'docs', 'api');
const OUTPUT_PATH = path.join(OUTPUT_DIR, 'reference.md');

// ── widdershins options ────────────────────────────────────────────────────
//
// omitHeader        – omit the HTML/YAML front-matter block that some static
//                     site generators expect; set to `true` for plain Markdown
// shallowSchemas    – render schemas inline instead of expanding every $ref;
//                     keeps the output readable for moderate-size APIs
// expandBody        – expand request-body schemas in-place so readers can see
//                     required fields without jumping to the Schemas section
// codeSamples       – generate code examples for every endpoint
// language_tabs     – languages to include in code examples; add or remove
//                     entries to match your team's preferred clients
// tocSummary        – use the `summary` field (first line of the description)
//                     as the heading in the Table of Contents
// headingLevel      – starting Markdown heading depth (1 = `#`, 2 = `##`, …)
// user_templates    – (optional) path to a folder of custom Mustache templates
//                     if you need to alter the output format in the future

const WIDDERSHINS_OPTIONS = {
  omitHeader:    false,     // keep the YAML front-matter (useful for Docusaurus/Jekyll)
  shallowSchemas: false,    // fully expand $ref schemas
  expandBody:    true,      // show request-body fields inline
  codeSamples:   true,      // generate code examples per endpoint
  language_tabs: [
    { 'shell': 'cURL' },
    { 'javascript': 'JavaScript' },
    { 'python': 'Python' },
  ],
  tocSummary:    true,      // use summary as ToC label
  headingLevel:  1,         // top-level headings start at H1
  summary:       true,      // include the info.summary in the document title
};

// ── Main ───────────────────────────────────────────────────────────────────

async function main() {
  // ── Step 1: Read the OpenAPI JSON file ──────────────────────────────────
  //
  // The file was generated by `scripts/generate-openapi.js` which boots a
  // lightweight Nest application and calls SwaggerModule.createDocument().
  // We parse it here as a plain JavaScript object that widdershins accepts.

  if (!fs.existsSync(INPUT_PATH)) {
    console.error(`ERROR: OpenAPI spec not found at ${path.relative(ROOT_DIR, INPUT_PATH)}`);
    console.error('Run `pnpm docs:openapi` first to generate it from NestJS controllers.');
    process.exit(1);
  }

  console.log(`Reading  ${path.relative(ROOT_DIR, INPUT_PATH)}`);
  const openApiSpec = JSON.parse(fs.readFileSync(INPUT_PATH, 'utf8'));

  // ── Step 2: Convert to Markdown via widdershins ──────────────────────────
  //
  // widdershins.convert() is asynchronous. It returns a Promise that resolves
  // to the final Markdown string.  The second argument is the options object
  // defined above.

  console.log('Converting via widdershins …');
  const markdown = await widdershins.convert(openApiSpec, WIDDERSHINS_OPTIONS);

  // ── Step 3: Write the output file ────────────────────────────────────────
  //
  // We write to docs/api/README.md so that GitHub and most documentation
  // platforms display it automatically when browsing to the docs/api/ folder.

  fs.mkdirSync(OUTPUT_DIR, { recursive: true });
  fs.writeFileSync(OUTPUT_PATH, markdown, 'utf8');

  console.log(`Generated ${path.relative(ROOT_DIR, OUTPUT_PATH)}`);
}

main().catch((err) => {
  console.error('Failed to generate API Markdown documentation.');
  console.error(err);
  process.exit(1);
});
